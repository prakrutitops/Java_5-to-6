package com.a265;

public class SingleDimEx 
{
	public static void main(String[] args) 
	{
		
		int marks[] = new int[5];
		
		marks[0]=10;
		marks[1]=20;
		marks[2]=30;
		marks[3]=40;
		marks[4]=50;
		
		
		System.out.println(marks[1]);
		
		System.out.println("--------------------");
		
		for(int i=0;i<5;i++)
		{
			System.out.println(marks[i]);
		}
		
		
	}
}
