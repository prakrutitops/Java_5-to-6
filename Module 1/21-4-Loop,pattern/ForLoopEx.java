package com.a214;

public class ForLoopEx 
{
	public static void main(String[] args) 
	{
		int i;
		
		//increment
		
		for(i=1;i<=50;i++)
		{
			System.out.println(i);
		}
		
		//decrement
		System.out.println("-------------------");
		
		for(i=10;i>=1;i--)
		{
			System.out.println(i);
		}
	}
}
